package com.sgtesting.text;

public class VowelsCharArray {

	public static void main(String[] args) {
		char ch[][]= {{'A','B','C'},{'D','E','F'},{'G','H','I'}};
		for(int i=ch.length;i<ch.length;i++)
		{
			for(int j=0;j<ch[i].length;j++)
				System.out.print(ch[i][j]+" ");
		}
		System.out.println();
	}

}
